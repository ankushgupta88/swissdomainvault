                      <h5 class="fw-bold pb-4"> WHAT IS SSL? </h5> 
                      <p>SSL Certificates enable data encryption on the internet and allow data to be transmitted securely from a web server to a browser. With SSL, your website can use the https protocol and will display a padlock in end users web browsers to indicate the connection is secure.</p>
                      <p>Although all SSL certificates use similar methods to protect and validate your data, the level of trust and assertion they provide varies. Standard SSL (Domain Validated) Certificates are the easiest and most common type of SSL certificate. OV and EV Certificates also authenticate the identity of the company or organization that holds the certificate providing more trust to end users.</p>
                      <p>EV Certificates offer the highest level of validation and trust, and will show the name of the company or organization in green in the address bar. This is an immediate, visual way that viewers can know that extra steps were taken to confirm the site they’re visiting – which is why most large companies and organizations choose EV certificates.</p>
